import mongoose from 'mongoose';

let Schema = mongoose.Schema;

let userSchema = new Schema({
  email: {
    type: String,
    lowercase: true,
    unique: true,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ['Client', 'Manager', 'Admin'],
    default: 'Client'
  }
});

module.exports = mongoose.model('User', userSchema);
